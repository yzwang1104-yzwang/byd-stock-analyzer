# 调休工作日 — 周末但需要补班开市
_WORKDAYS: dict[str, str] = {
    # 春节前补班
    "2026-02-14": "春节调休",
    "2026-02-15": "春节调休",
    # 国庆前补班
    "2026-09-20": "国庆调休",
}


def is_trading_day(d: date | None = None) -> tuple[bool, str]:
    """判断是否为 A 股交易日。

    Args:
        d: 日期，默认今天

    Returns:
        (is_trading, reason): True=开市, False=休市, 附带原因
    """
    if d is None:
        d = date.today()

    ds = d.isoformat()

    # 1. 调休工作日 → 开市
    if ds in _WORKDAYS:
        return True, _WORKDAYS[ds]

    # 2. 周末 → 休市
    if d.weekday() >= 5:  # 5=周六, 6=周日
        return False, "周末"

    # 3. 法定节假日 → 休市
    if ds in _HOLIDAYS:
        return False, _HOLIDAYS[ds]

    # 4. 正常工作日 → 开市
    return True, "正常交易日"


def next_trading_day(d: date | None = None) -> date:
    """返回下一个交易日。"""
    if d is None:
        d = date.today()
    d += timedelta(days=1)
    while not is_trading_day(d)[0]:
        d += timedelta(days=1)
    return d
